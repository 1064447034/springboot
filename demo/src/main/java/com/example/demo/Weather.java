package com.example.demo;

import java.util.Date;

public class Weather {
	private Date date;
	private String city;
	private String wez;
	
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getWez() {
		return wez;
	}
	public void setWez(String wez) {
		this.wez = wez;
	}
	
	
}
